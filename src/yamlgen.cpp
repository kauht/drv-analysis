// Single-quote a YAML scalar, doubling any embedded single quotes -- the
// simplest quoting rule that's always safe for arbitrary free text (dates,
// descriptions, copyright strings that may contain colons or '#').
std::string yq(const std::string& s) {
    std::string out = "'";
    for (char c : s) {
        if (c == '\'')
            out += "''";
        else
            out.push_back(c);
    }
    out += "'";
    return out;
}

// A YAML list of scalars, one per line, at the given indent (spaces).
std::string yaml_list(const std::vector<std::string>& items, int indent) {
    std::string pad(indent, ' ');
    std::ostringstream out;
    if (items.empty()) {
        out << pad << "[]\n";
        return out.str();
    }
    for (const auto& it : items)
        out << pad << "- " << it << "\n";
    return out.str();
}

std::string today_utc() {
    auto t = std::time(nullptr);
    std::tm tm{};
    gmtime_s(&tm, &t);
    char buf[16];
    std::strftime(buf, sizeof(buf), "%Y-%m-%d", &tm);
    return buf;
}

// PE TimeDateStamp -> "YYYY-MM-DD HH:MM:SS", matching LOLDrivers' format.
std::string format_pe_timestamp(uint32_t unix_time) {
    std::time_t t = static_cast<std::time_t>(unix_time);
    std::tm tm{};
    gmtime_s(&tm, &t);
    char buf[32];
    std::strftime(buf, sizeof(buf), "%Y-%m-%d %H:%M:%S", &tm);
    return buf;
}

// A random UUID v4 (RFC 4122), used as the entry Id for a brand-new driver
// family. If this sample belongs to an EXISTING LOLDrivers entry (a known
// driver name, new version/build), use that entry's Id instead -- don't
// mint a duplicate family for the same driver.
std::string random_uuid4() {
    std::random_device rd;
    std::mt19937_64 gen(rd());
    std::uniform_int_distribution<uint64_t> dist;
    uint64_t hi = dist(gen), lo = dist(gen);
    uint8_t bytes[16];
    std::memcpy(bytes, &hi, 8);
    std::memcpy(bytes + 8, &lo, 8);
    bytes[6] = (bytes[6] & 0x0F) | 0x40;  // version 4
    bytes[8] = (bytes[8] & 0x3F) | 0x80;  // variant 1

    static const char* hexd = "0123456789abcdef";
    std::string out;
    auto put_byte = [&](uint8_t b) {
        out.push_back(hexd[b >> 4]);
        out.push_back(hexd[b & 0xF]);
    };
    for (int i = 0; i < 16; ++i) {
        if (i == 4 || i == 6 || i == 8 || i == 10)
            out.push_back('-');
        put_byte(bytes[i]);
    }
    return out;
}

double shannon_entropy(const uint8_t* data, size_t len) {
    if (len == 0)
        return 0.0;
    size_t counts[256] = {0};
    for (size_t i = 0; i < len; ++i)
        counts[data[i]]++;
    double entropy = 0.0;
    for (size_t c : counts) {
        if (c == 0)
            continue;
        double p = static_cast<double>(c) / static_cast<double>(len);
        entropy -= p * std::log2(p);
    }
    return entropy;
}

std::string hex_double(double d) {
    std::ostringstream ss;
    ss << d;
    return ss.str();
}

std::string to_hex_field(uint32_t v) {
    std::ostringstream ss;
    ss << "0x" << std::hex << v;
    return ss.str();
}

